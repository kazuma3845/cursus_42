#include "libft.h"

int	ft_intlen(long n)
{
	int	i;
	int	neg;

	i = 0;
	neg = 0;
	if (n == 0)
		return (1);
	if (n < 0)
	{
		n *= -1;
		neg = 1;
	}
	while (n > 0)
	{
		n = n / 10;
		i++;
	}
	if (neg == 1)
		return (i + 1);
	return (i);
}
