#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*new_list;
	t_list	*new_elem;
	t_list	*temp;

	if (!lst || !f)
		return (NULL);
	new_list = NULL;
	temp = lst;
	while (temp)
	{
		new_elem = ft_lstnew(f(temp->content));
		if (!new_elem)
		{
			ft_lstclear(&new_list, del);
			return (NULL);
		}
		ft_lstadd_back(&new_list, new_elem);
		temp = temp->next;
	}
	return (new_list);
}
